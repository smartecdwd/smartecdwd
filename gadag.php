                    <p style="font-family:Average;font-size:17px;color:#000;margin:10px;"> PG Centre at Gadag was started in the year 2000 by Karnatak University Dharwad with an objective to provide an opportunity for the  students of the Gadag District and surrounding places. The PG Centre first introduced Post Graduate programme in Commerce (M.Com) in the year 2000 and subsequently added PGDCA at Tontadarya College, Gadag. Realising growing demand and to provide easy access to the students of the Gadag District, two years full time MBA programme was introduced from the academic year 2014-15. In the year 2016 MSW course was introduced. Presently KUPG Centre,Gadag possesses 32 Acres of land at the outskirts of Gadag city adjacent to Kalasapur village with a building comprised of 10 spacious rooms.</p>
