		    <p>The Extra-mural or Extension wing, the "Prasaranga" is the publication and the Extension wing of the University. The Prasaranga was established in the year 1949. Within three years of the establishment of Karnatak University, its founders succeeded in launching Extramural or Extension activities of the University so that the knowledge generated by its teachers and researchers is carried to the door steps of the common man. A committee, with Shri Channaveera Kanavi, now a celebrated Kannada poet, as its Secretary was constituted and it started extension lectures in the mofussil areas. The first lecture camp was arranged at Gadag on 8th and 9th of November, 1952 marking the beginning of a worthwhile and long sustained activity. Very soon a publication wing was added and the two wings, Extension and Publication, merged to form the 'Prasaranga' with Shri Kanavi as its first Director.
            </p>
<p>The Publication division of Prasaranga has been serving as the medium for publishing the results of researches carried out in the Post Graduate Departments of the Karnatak University and the colleges affiliated to it. The books belonging to different subjects and areas of knowledge are being published by the Prasaranga. Books, theses, research papers, review, monographs, epics, text books, proceedings containing the lectures delivered under different series, periodicals in Science, Arts and Social Science both in English and Kannada are being published.</p>
<p>It has now emerged as one of the largest publishers in Karnataka. It has published nearly 2000 titles, some of which have gone into several editions.</p>
<br>
<p><b>There are five areas in which it has made a distinctive name :</b></p>
    <ul style="font-family:Average;font-size:16px;color:#000;margin:10px;">
		<li>Publication of pocket-size booklets on various useful subjects for the benefit of common people.</li>
		<li>Publication of books in Kannada and English on Vachana literature, a distinct form of prose-poems composed by Shiva-sharana saints who heralded social and religious revolution in Karnataka. A few of the Vachanas are translated into Hindi, French, German, Marathi and Tamil book form. There are nearly 100 titles belonging to this category.</li>
		<li>Publication in the field of Kannada folk-lore, especially in two series Jeevana Jokali and Janapada Sahitya Darshana, the latter having its 25th volume in Press.</li>
		<li>Ph.D. theses and other Research Publications, 57 by Prasaranga and 30 by others with our assistance</li>
		<li>Special Lecture Series publications - 45 Titles.</li>

		</ul><br>

<p>The main objective of Extramural or Extension Education wing is to conduct lecture camps not only in remote areas coming under the jurisdiction of the Karnatak University (7districts) but also outside the four walls of the University Campus. The idea behind this activity is to share the treasure of knowledge generated inside the University with the common man. The University has the unique honour of conducting largest number of lecturers in rural areas, when compared to other Universities in Karnataka State or any other state in India. By March 2002, the Prasaranga has arranged more than 2000 Lectures in 454 lecture camps in the mofussil places covering subjects as varied as agricultural implements, biological diversity, changes in Indian villages, poultry, menace of drugs, consumer protection, pollution, AIDS, female infanticide, water resources, Lok Adalat, poets, epics, folk tales. Of the 2000 and odd lectures, some 525 are already published in the form of low-priced booklets, which is a record for such publications.
</p>
		
		<p>Another series, slightly different from the earlier one was started in 1962 and this is called 'Comprehensive Literature Series'. It caters to the people of towns having colleges or such other institutions of higher learning. In this series, 6 to 8 specialists deliberate on various faces of a single topic. 27 books have been published in this series.</p>
		<p>The Special Lecture series which was started in 1950 is yet another landmark in the activities of 'Prasaranga'. The main purpose of holding Special Lectures is to invite the scholars of eminence at the national level to deliver lectures and publish proceedings in book form. These lectures are delivered either in English or in Kannada. To name only a few of the eminent personalities who delivered these lectures-Gurudev, R. D. Ranade, B.A. Saletore, R.D. Karmarkar, Mulk Raj Anand, Shishir Kumar Ghosh, V.K. Gokak, Arthur Ravenoroft, D. Raimons Needor, Balachandra Nemade, V. Sitaramaiah, Shivaram Karant, D.R. Bendre, G.S.Shivarudrappa, M. Chidananda Murthy, Shantinath Desai, 43 publications are brought out under this Special Lecture series so far.</p>
		<p>Translation of VACHANAS into English and other Indian languages was carried out extensively in the past. Recently Prasaranga has brought out a translation of the "Manasollas", a 14th century Encyclopedia in Sanskrit on land and people of Karnataka in 2 volumes. Prasaranga has also sponsored a series of A.I.R. broadcasts introducing the Prasaranga and its publications to the general public. It has arranged a large number of book exhibitions all through these years.</p>
		<p>The Prasaranga of Karnatak University, after having realized the importance of Journals of scientific pursuits, research contributions etc., has undertaken the responsibility of publishing five journals, namely Karnatak Bharati (Kannada Biannual), Vidyarthi Bharati (Kannada Biannual for students), K.U. Journal in Science (English Annual), K.U. Journal in Humanities (English Annual) and K.U. Journal in Social Science. The purpose of bringing these periodicals is to encourage research activity among the teachers and literary expression of students of the P.G. and under-graduate classes.</p>
		<p>The two types of activities of the Prasaranga mentioned above are guided by two separate committees constituted by the Syndicate of this University, the former by the Extension and Extra Mural Board and the latter by the Publication Board presided over by the Vice-Chancellor.</p>
		<p>In the words of Dr. Navaneet Rao, Chairman, Karnatak Universities Review Committee, "The Prasaranga is an effective forum for teachers in both post-graduate departments and affiliated colleges to publish their findings" (Navaneet Rao; Report of the Karnatak Universities Review Commission, 1993).</p>
		
		<p>'Prasaranga' is proud of its achievements so far and its role in furthering education of classes and masses, particularly in the north Karnataka area and hopes to scale new heights.</p>
		
		
