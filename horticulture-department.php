<h5><b>BOTANICAL GARDEN</b></h5>
<p>The Karnatak University Botanic Garden has been established in 1963. It is the only Botanic Garden in northern part of Karnataka state. It spreads in an area of over 40 acres and is located at elevation of 830 meters. The flowering plants represent 170 families and more than 1300 sps. have been maintained. This garden also has a small nursery where medicinal plants, rare and ornamental plants are maintained. It is the member of BGCI and IBGN. The Garden is well planned with different sections like 1. Oil yielding plant, 2. Economic plants, 3. Garcinia section, 4. Coconut garden, 5. Citrus garden, 6. Systematic section and 7. Gymnosperms and local flora section. The Garden has both indigenous and exotic sps. from different places.</p>
<p>The Garden is a centre of study for students of M.Sc. Botany and various other departments like Zoology and Chemistry. This garden is a centre of attraction for public, nature lovers and students of different Colleges, including Ayurvedic and Homeopathy Colleges and other Universities for the study.
</p>
