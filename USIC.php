        <h5 class="mt-4"><b>ABOUT THE CENTER:</b></h5>
        <p>It is a multidisciplinary Research and Educational core facility started in 1976 with the initial support of UGC and is at level III as per UGC recommendation. It has multi‐purpose cluster of sophisticated analytical and bioimaging instruments (Table1) serving the community of UG & PG students, research scholars and Faculty of Science Departments of KUD and other academic Institutes/researchlabs/industries. These instruments cater to the needs of physical, chemical, biological and earth sciences in addition to other fields including nano‐science, polymer science, materials science, biotechnology, pharmaceutical, medical and agricultural sciences. The instruments are maintained by the qualified and dedicated Technical Staﬀ (Table2). Besides oﬀering services, production and distribution of liquid nitrogen and Milli‐Q water is also done.</p><br>
        <h5><b>PURSE AND SAIF PROGRAMMES:</b></h5>
        <p>Recognizing the Karnatak University, as one among 14 in the country for its performance in research, the Department of Science and Technology (DST), New Delhi, sanctioned PURSE (Rs. 9 Crores) Program for augmenting instrumental facilities at USIC to enable the researchers to carryout comprehensive and advanced research in frontier areas. Implementing PURSE in 2009 at USIC, 10 instruments were procured in two phases, Installed and dedicated to researchers. In II phase of the PURSE Program, the DST had sanctioned Rs.15 Crores to continue the program and to procure state‐of‐the‐art instrumental facilities.
         </p><br>
        <p>Further, the DST, New Delhi had sanctioned Rs 5.5 crores in 2014‐15 to set up a Sophisticated Analytical Instruments Facility (SAIF) Center at the Karnatak University. This was established and procured the sophisticated instruments namely (i) 400 MHz FT‐NMR Spectrometer (ii) Powder X‐ray Diﬀractometer, (iii)Scanning Electron Microscope with EDS and (iv) CD Spectrometer and dedicated to researchers. Further, a grant of Rs. 67,50,241=00 was sanctioned in 2019-20 and Rs. 58,00,000=00 in 2021-22 for the purchase of CD Spectrometer and Liquid Nitrogen Generator, respectively.</p><br>
