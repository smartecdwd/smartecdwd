<h5><b>ABOUT THE CENTER:</b></h5>
<p>It is a multidisciplinary research and educational core facility started in 1976 with the initial support of UGC and is at level III as per UGC recommendation. It has multi-purpose cluster of sophisticated analytical and bioimaging instruments (Table 1) serving the community of UG & PG students, research scholars and faculty of Science Departments of KUD and other academic Institutes/research labs/industries. These instruments cater to the needs of physical, chemical, biological and earth sciences in addition to other fields including nano-science, polymer science, materials science, biotechnology, pharmaceutical, medical and agricultural sciences. The instruments are maintained by the qualified and dedicated technical sta? (Table 2). Besides o?ering services, production and distribution of liquid nitrogen and Milli-Q water is also done.</p><br>
<h5><b>PURSE AND SAIF PROGRAMMES:</b></h5>
<p>Recognizing the Karnatak University, Dharwad, as one among fourteen in the country for its performance in research, the Department of Science and Technology (DST), New Delhi, sanctioned PURSE (Rs 9 Crores) Program for augmenting instrumental facilities at USIC to enable the researchers to carryout comprehensive and advanced research in frontier areas. Implementing PURSE in the year 2009 at USIC, ten instruments were procured in two phases, installed and dedicated to researchers. In the second phase of the PURSE Program, the DST had sanctioned Rs. 15 Crores to continue the program and to procure state-of-the- art instrumental facilities and infrastructure.</p><br>
