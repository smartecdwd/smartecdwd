<h5><strong>INTRODUCTION</strong></h5>

<p style="font-family:Average;font-size:17px;color:#000;margin:10px">Karnatak University Post Graduate Centre, Haveri was established in the year 2001 to fulfill the aspirations of rural and urban Students of Haveri district of Karnataka State. The P.G.Centre is located at Kerimattihalli, about 8 kms from Haveri and spread over and area of 48 acres. Six Post-Graduate departments namely Commerce, Kannada, English, Sociology, Social Work, Mass communication and Journalism are offered. CBCS Pattern is adopted in all the P G Courses. Academic programmes and examinations are conducted on schedule and in time as per the Karnatak university guidelines.</p>

<p style="font-family:Average;font-size:17px;color:#000;margin:10px">The P.G. Centre maintains a close liaison with the public and eminent local leaders of Haveri through various meaningful programmes including induction, interaction, extension activities through NSS. True to its vision and mission, the P.G. Centre, is committed to reach greater heights of excellence in the years to come.</p>
